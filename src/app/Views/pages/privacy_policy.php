                    <p>
                        แอปพลิเคชัน Command Book ("เรา") ให้ความสำคัญอย่างยิ่งต่อการคุ้มครองข้อมูลส่วนบุคคลของผู้ใช้งาน ("ท่าน") และเพื่อให้เป็นไปตามพระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562 ("PDPA") เราจึงได้จัดทำนโยบายฉบับนี้ขึ้นเพื่อแจ้งให้ท่านทราบถึงรายละเอียดเกี่ยวกับการเก็บรวบรวม ใช้ และ/หรือเปิดเผยข้อมูลส่วนบุคคลของท่าน
                    </p>

                    <hr class="my-4">

                    <h5>1. ข้อมูลส่วนบุคคลที่เราเก็บรวบรวม</h5>
                    <p>เราจะเก็บรวบรวมข้อมูลส่วนบุคคลของท่านเท่าที่จำเป็นภายใต้วัตถุประสงค์อันชอบด้วยกฎหมาย ดังนี้:</p>
                    <ul>
                        <li><strong>ข้อมูลที่ใช้ในการยืนยันตัวตน:</strong> ชื่อ-นามสกุล, ชื่อผู้ใช้ (Username), รหัสผ่าน (ที่ผ่านการเข้ารหัส)</li>
                        <li><strong>ข้อมูลการเข้าสู่ระบบผ่าน LINE:</strong> Profile จาก LINE (เช่น ชื่อ, รูปโปรไฟล์, User ID) และอีเมล (หากท่านอนุญาต)</li>
                        <li><strong>ข้อมูลระบุหน่วยงาน:</strong> รหัสหน่วยบริการ (Hospcode) และชื่อหน่วยบริการที่ท่านสังกัด</li>
                        <li><strong>ข้อมูลทางเทคนิค:</strong> หมายเลขที่อยู่ไอพี (IP Address), ประเภทของเบราว์เซอร์, ข้อมูลการใช้งานเว็บไซต์และแอปพลิเคชัน (Cookies)</li>
                        <li><strong>ข้อมูลการใช้งาน:</strong> ประวัติการอัปโหลดเอกสาร, ประวัติการเข้าถึงเอกสาร</li>
                    </ul>

                    <h5>2. วัตถุประสงค์ในการเก็บรวบรวมและใช้ข้อมูลส่วนบุคคล</h5>
                    <ul>
                        <li>เพื่อการยืนยันตัวตนและอนุญาตให้เข้าถึงบริการของแอปพลิเคชัน</li>
                        <li>เพื่อจัดการสิทธิ์การเข้าถึงเอกสารตามหน่วยงานที่ท่านสังกัด</li>
                        <li>เพื่อบันทึกและตรวจสอบประวัติการใช้งาน (Audit Trail) ตามข้อกำหนดด้านความปลอดภัย</li>
                        <li>เพื่อปรับปรุงและพัฒนาคุณภาพการให้บริการของแอปพลิเคชัน</li>
                        <li>เพื่อปฏิบัติตามกฎหมายและข้อบังคับที่เกี่ยวข้อง</li>
                    </ul>

                    <h5>3. การเปิดเผยข้อมูลส่วนบุคคล</h5>
                    <p>เราจะไม่เปิดเผยข้อมูลส่วนบุคคลของท่านต่อบุคคลภายนอกโดยไม่ได้รับความยินยอมจากท่าน เว้นแต่ในกรณีดังต่อไปนี้:</p>
                    <ul>
                        <li>เพื่อปฏิบัติตามบทบัญญัติของกฎหมาย หรือตามคำสั่งศาล หรือตามคำสั่งของหน่วยงานของรัฐที่มีอำนาจ</li>
                        <li>ในกรณีที่จำเป็นเพื่อปกป้องความปลอดภัยในชีวิตและทรัพย์สินของเราหรือของบุคคลอื่น</li>
                    </ul>

                    <h5>4. ระยะเวลาในการจัดเก็บข้อมูล</h5>
                    <p>เราจะจัดเก็บข้อมูลส่วนบุคคลของท่านไว้ตราบเท่าที่จำเป็นเพื่อวัตถุประสงค์ที่ระบุไว้ในนโยบายนี้ หรือตราบเท่าที่ท่านยังคงสถานะเป็นผู้ใช้งานในระบบ และจะจัดเก็บต่อไปตามระยะเวลาที่กฎหมายกำหนดหรือตามนโยบายการจัดเก็บเอกสารขององค์กร</p>

                    <h5>5. มาตรการรักษาความมั่นคงปลอดภัย</h5>
                    <p>เราใช้มาตรการรักษาความปลอดภัยทางเทคนิคและการบริหารจัดการที่เหมาะสม เพื่อป้องกันการเข้าถึง การใช้ หรือการเปิดเผยข้อมูลส่วนบุคคลโดยไม่ได้รับอนุญาต ซึ่งรวมถึงการเข้ารหัสข้อมูลที่สำคัญ และการจำกัดสิทธิ์การเข้าถึงข้อมูลสำหรับเจ้าหน้าที่ที่เกี่ยวข้องเท่านั้น</p>

                    <h5>6. สิทธิของเจ้าของข้อมูล</h5>
                    <p>ท่านมีสิทธิตามกฎหมายคุ้มครองข้อมูลส่วนบุคคล ดังนี้:</p>
                    <ul>
                        <li>สิทธิในการเข้าถึงและขอรับสำเนาข้อมูลส่วนบุคคลของท่าน</li>
                        <li>สิทธิในการขอแก้ไขข้อมูลส่วนบุคคลของท่านให้ถูกต้อง</li>
                        <li>สิทธิในการคัดค้านการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคล</li>
                        <li>สิทธิในการขอให้ลบหรือทำลายข้อมูลส่วนบุคคลของท่าน (ภายใต้เงื่อนไขที่กฎหมายกำหนด)</li>
                        <li>สิทธิในการขอให้ระงับการใช้ข้อมูลส่วนบุคคลของท่าน</li>
                    </ul>

